public class main {
    public static void main(String[] args){
        // decalre variable a and b 
        int a = 20, b=10;
        int result;
      
        // addition operator
        result=a+b;
        System.out.println(result);

        // Substraction operator
        System.out.println(a-b);

        // we can directly perform substraction in print statement,no need to
        // use result variable here

        // multipication operator
        System.out.println(a * b);

        // divide operator
        System.out.println(a / b);

        // module operator
        System.out.println(a % b);

    }
}
